-- SELF JOIN--
SELECT C1.CUSTOMERID,C1.CUSTOMERNAME,C1.ADDRESS,C1.CITY,C1.POSTALCODE,C1.STATE,C1.COUNTRY FROM CUSTOMER C1,CUSTOMER C2
