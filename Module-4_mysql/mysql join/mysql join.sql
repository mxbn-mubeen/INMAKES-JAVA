-- CROSS JOIN--
SELECT ORDERID,ORDERS.CUSTOMERID /* ORDERS TABLE*/,CUSTOMERNAME,ITEM,ORDERDATE 
FROM ORDERS CROSS JOIN CUSTOMER;

